package courseraWeek2;

public  class ExpressionIfSwitch {

    void printColorDescription (Color colorKotlin) {
        switch ( colorKotlin) {
            case BLUE:
                System.out.println("cold");
                break;
            case ORANGE:
                System.out.println("mild");
                break;
            default:
                System.out.println();

        }

    }

}
